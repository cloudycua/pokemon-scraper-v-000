class Pokemon
  attr_accessor :id, :name, :type, :db
  @@all = []

  def initialize(id:, name:, type:, db:)
    @id = id
    @name = name
    @type = type
    @db = db
  end

  def self.save(name, type, db)
    database_connection.execute ("INSERT INTO #{db} (name, type) VALUES (?,?)", name, type)
  end

end
